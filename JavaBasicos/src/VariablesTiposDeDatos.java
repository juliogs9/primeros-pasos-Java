
public class VariablesTiposDeDatos {
	
	public static void main(String[] args) {
		
		
	// String permite almacenar cadenas de texto 
		
// _____ En java hay que ser mas especifico que tipo de texto es mi variables
		
		String nombre = "Julio";
		int edad = 31;
		
		
// _____________  char solo guarda un caracter	
		
		char initial = 'J'; 
		
// ______________ boolean almacena boleanos true false
		
		boolean tienesCrush = true;
		
// ______________  Tipos de datos enteros 
		
		// >>> byte 
		
		byte vejedad = 31;
		
		
		
		// >>> short tendra un limite de almacenar informacion -127 a 128 256 caracteres en total
		
		short suma = 24 + 103;
		
		// >>> int 
		
		int numero = 1010101010;
		
		// >>> long 
		
		long numeroLargo = 100_000_000;
		
		// >>> float siempre poner una ( f ) al final para que sepa que un float
		
		float promedio = 1200.2222f;
		
		// >>> double 
		
		double numeroPi = 3.1416999;
		
	}

}
